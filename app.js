function passwordScore(value) {
  let score = 0;
  if (value.length >= 8) score += 25;
  if (value.length >= 12) score += 20;
  if (/\p{L}/u.test(value)) score += 15;
  if (/[0-9]/.test(value)) score += 15;
  if (/[^\p{L}0-9]/u.test(value)) score += 20;
  if (!/(123|password|qwerty|2015|2016|2017|2018|2019|2020|haim|noam)/i.test(value)) score += 5;
  return Math.min(score, 100);
}

