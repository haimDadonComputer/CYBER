function loadState() {
  try {
    return { sceneIndex: 0, lineIndex: 0, badges: [], clues: [], feedback: {}, orders: {}, ...JSON.parse(localStorage.getItem(STORAGE_KEY)) };
  } catch {
    return { sceneIndex: 0, lineIndex: 0, badges: [], clues: [], feedback: {}, orders: {} };
  }
}

function saveState() {
  localStorage.setItem(STORAGE_KEY, JSON.stringify(state));
}

function currentScene() {
  return scenes[state.sceneIndex] || scenes[0];
}

function normalizeStateForScenes() {
  state.sceneIndex = Math.max(0, Math.min(state.sceneIndex || 0, scenes.length - 1));
  const active = currentScene();
  state.lineIndex = Math.max(0, Math.min(state.lineIndex || 0, active.dialogue.length - 1));
}

function missionById(id) {
  return missions.find((mission) => mission.id === id);
}

function setScene(index, lineIndex = 0) {
  state.sceneIndex = Math.max(0, Math.min(index, scenes.length - 1));
  state.lineIndex = Math.max(0, lineIndex);
  saveState();
  render();
}

function advance() {
  const active = currentScene();
  if (state.lineIndex < active.dialogue.length - 1) {
    state.lineIndex += 1;
    saveState();
    render();
    return;
  }
  if (state.sceneIndex < scenes.length - 1) setScene(state.sceneIndex + 1);
}

function back() {
  if (state.lineIndex > 0) {
    state.lineIndex -= 1;
    saveState();
    render();
    return;
  }
  const previous = scenes[state.sceneIndex - 1];
  setScene(state.sceneIndex - 1, previous ? previous.dialogue.length - 1 : 0);
}

function completeMission(mission) {
  if (!state.badges.includes(mission.id)) state.badges.push(mission.id);
  if (!state.clues.includes(mission.id)) state.clues.push(mission.id);
  saveState();
}

function render() {
  normalizeStateForScenes();
  const active = currentScene();
  document.title = `${active.title} - דיגיטוליה`;
  renderProgress();
  renderJumpSelect();
  prevBtn.disabled = state.sceneIndex === 0 && state.lineIndex === 0;
  nextBtn.textContent = state.lineIndex < active.dialogue.length - 1 ? "המשך" : (state.sceneIndex === scenes.length - 1 ? "סיום" : "הבא");
  renderStoryScene(active);
}

function renderProgress() {
  progressStrip.innerHTML = missions.map((mission) => `<span class="progress-dot ${state.badges.includes(mission.id) ? "done" : ""}" title="${mission.badge}">${mission.number}</span>`).join("");
}

