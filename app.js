let selectedSymptom = null;
function attachMalwareHandlers() {
  selectedSymptom = null;
  document.querySelectorAll(".symptom").forEach((button) => {
    button.addEventListener("click", () => {
      selectedSymptom = button;
      document.querySelectorAll(".symptom").forEach((item) => item.classList.remove("selected"));
      button.classList.add("selected");
      document.getElementById("malwareFeedback").textContent = "עכשיו מחפשים את השלט שמתאים לתסמין.";
    });
  });
  document.querySelectorAll(".category").forEach((button) => {
    button.addEventListener("click", () => {
      const feedback = document.getElementById("malwareFeedback");
      if (!selectedSymptom) return feedback.textContent = "קודם בוחרים תסמין.";
      if (selectedSymptom.dataset.type === button.dataset.type) {
        selectedSymptom.disabled = true;
        selectedSymptom.style.opacity = "0.45";
        feedback.textContent = "פענוח טוב.";
        selectedSymptom = null;
      } else {
        feedback.textContent = "כמעט. האם זה מקלקל, מציץ או תקלה רגילה?";
      }
    });
  });
}

function attachTaskHandlers() {
  document.querySelectorAll(".process-check").forEach((button) => {
    button.addEventListener("click", () => {
      const feedback = document.getElementById("taskFeedback");
      if (button.dataset.risk === "high") {
        feedback.textContent = "מצוין. תוכנה עם שימוש גבוה מאוד דורשת בדיקה.";
        button.closest("tr").style.background = "rgba(93, 242, 165, 0.2)";
      } else if (button.dataset.risk === "mid") {
        feedback.textContent = "יכול להיות עומס רגיל. הרבה לשוניות לוקחות מקום.";
      } else {
        feedback.textContent = "זה נראה רגוע יחסית.";
      }
    });
  });
}

